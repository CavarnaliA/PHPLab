<?php

/**
 * ValidatorInterface.php
 *
 * Интерфейс для валидаторов данных.
 *
 * Определяет обязательный метод validate, который
 * должен проверять входные данные и возвращать массив ошибок.
 *
 * Используется для реализации различных типов валидации
 * в приложении (например, учебных сессий).
 */

interface ValidatorInterface
{
    /**
     * Валидирует входные данные.
     *
     * @param array $data входные данные для проверки
     * @return array массив ошибок (если есть), иначе пустой массив
     */
    public function validate(array $data): array;
}
